package Methods;

public class Methods_nor

{

// methods overloading
	
	void m1()
	{
		System.out.println("m1");
	}
	
	void m1(int x)
	{
		System.out.println(x);
	}
	
	void m1(int x,double y)
	{
		System.out.println(x + "" + y);
	}
	
	void m1(double y, int x)
	{
		System.out.println(y + "" + x);
	}

	
	
}
